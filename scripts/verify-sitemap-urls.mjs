/**
 * Probe every <loc> in public/sitemap.xml against production (or BASE_URL).
 * Writes docs/sitemap-http-verification.md with non-2xx and anomaly summary.
 * Usage: node scripts/verify-sitemap-urls.mjs
 */

import fs from 'fs/promises';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');

const BASE_URL =
  process.env.BASE_URL?.replace(/\/$/, '') ?? 'https://justlegalsolutions.org';
const SITEMAP = path.join(ROOT, 'public', 'sitemap.xml');
const OUT_MD = path.join(ROOT, 'docs', 'sitemap-http-verification.md');

const CONCURRENCY = Number.parseInt(process.env.VERIFY_CONCURRENCY ?? '10', 10);
const TIMEOUT_MS = Number.parseInt(process.env.VERIFY_TIMEOUT_MS ?? '20000', 10);

async function probeOnce(url, method) {
  const controller = new AbortController();
  const tid = setTimeout(() => controller.abort(), TIMEOUT_MS);
  try {
    const res = await fetch(url, {
      method,
      redirect: 'follow',
      signal: controller.signal,
      headers: { 'User-Agent': 'JLS-sitemap-verify/1.0' },
    });
    clearTimeout(tid);
    return { ok: res.ok, status: res.status, finalUrl: res.url };
  } catch (e) {
    clearTimeout(tid);
    const msg = e.name === 'AbortError' ? 'TIMEOUT' : String(e.message);
    return { ok: false, status: msg, finalUrl: url };
  }
}

async function probe(url) {
  let r = await probeOnce(url, 'HEAD');
  if (typeof r.status === 'number' && (r.status === 405 || r.status === 501)) {
    r = await probeOnce(url, 'GET');
  }
  if (typeof r.status === 'number' && r.status === 405) {
    r = await probeOnce(url, 'GET');
  }
  return r;
}

async function main() {
  const xml = await fs.readFile(SITEMAP, 'utf8');
  const urls = [...xml.matchAll(/<loc>([^<]+)<\/loc>/g)].map((m) => {
    try {
      return new URL(m[1]).toString().replace(/\/$/, '');
    } catch {
      return m[1].trim().replace(/\/$/, '');
    }
  });

  const unique = [...new Set(urls)];
  /** @type {Array<{ url: string; status: number|string; ok: boolean; finalUrl: string }>} */
  const results = [];

  let i = 0;
  async function worker() {
    while (i < unique.length) {
      const idx = i++;
      const url = unique[idx];
      const r = await probe(url);
      results[idx] = { url, ok: r.ok, status: r.status, finalUrl: r.finalUrl };
    }
  }

  await Promise.all(
    Array.from({ length: Math.min(CONCURRENCY, unique.length) }, () => worker())
  );

  const bad = results.filter(
    (r) => !(typeof r.status === 'number' && r.status >= 200 && r.status < 400)
  );
  const timeouts = bad.filter((r) => r.status === 'TIMEOUT');
  const non2xxNonRedirect = bad.filter(
    (r) => !(typeof r.status === 'number' && r.status >= 300 && r.status < 400)
  );

  const now = new Date().toISOString();
  let md = `<!-- Generated by scripts/verify-sitemap-urls.mjs — DO NOT edit by hand; re-run npm run verify:sitemap -->\n\n`;
  md += `# Sitemap HTTP verification\n\n`;
  md += `- **When:** ${now}\n`;
  md += `- **Base:** ${BASE_URL}\n`;
  md += `- **URLs in <loc> (deduped):** ${unique.length}\n`;
  md += `- **Non-success (outside 200–399):** ${non2xxNonRedirect.length}\n`;
  md += `- **Timeouts:** ${timeouts.length}\n\n`;

  md += `## Rows outside success / redirect range\n\n`;
  md += bad.length === 0
    ? `_None — all probes returned HTTP 200–399._\n\n`
    : `| URL | Status | Final URL |\n|-----|--------|----------|\n`;
  for (const r of bad.sort((a, b) => String(a.status).localeCompare(String(b.status)))) {
    md += `| ${r.url.replace(/\|/g, '\\|')} | ${r.status} | ${String(r.finalUrl).replace(/\|/g, '\\|')} |\n`;
  }

  md += `\n## All results (compact)\n\n`;
  md += `<details>\n<summary>${unique.length} rows</summary>\n\n`;
  md += `| Status | URL |\n|--------|-----|\n`;
  for (const r of [...results].sort((a, b) => a.url.localeCompare(b.url))) {
    md += `| ${r.status} | ${r.url.replace(/\|/g, '\\|')} |\n`;
  }
  md += `\n</details>\n`;

  await fs.mkdir(path.dirname(OUT_MD), { recursive: true });
  await fs.writeFile(OUT_MD, md, 'utf8');
  console.log(`Wrote ${OUT_MD}`);
  console.log(`Anomalies (non-success): ${non2xxNonRedirect.length}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
