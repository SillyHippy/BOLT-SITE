/**
 * Layer 2: verify _redirects sources return 301 → canonical 200 on production.
 * Usage: node scripts/verify-redirects.mjs
 */

import fs from 'fs/promises';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');
const REDIRECTS = path.join(ROOT, 'public', '_redirects');
const OUT = path.join(ROOT, 'docs', 'redirect-http-verification.md');

const BASE =
  process.env.BASE_URL?.replace(/\/$/, '') ?? 'https://justlegalsolutions.org';
const TIMEOUT_MS = Number.parseInt(process.env.VERIFY_TIMEOUT_MS ?? '20000', 10);

/** @param {string} url */
async function headNoFollow(url) {
  const controller = new AbortController();
  const tid = setTimeout(() => controller.abort(), TIMEOUT_MS);
  try {
    const res = await fetch(url, {
      method: 'HEAD',
      redirect: 'manual',
      signal: controller.signal,
      headers: { 'User-Agent': 'JLS-redirect-verify/1.0' },
    });
    clearTimeout(tid);
    return {
      status: res.status,
      location: res.headers.get('location') ?? '',
    };
  } catch (e) {
    clearTimeout(tid);
    return { status: e.name === 'AbortError' ? 'TIMEOUT' : String(e.message), location: '' };
  }
}

/** @param {string} url */
async function headFollow(url) {
  const controller = new AbortController();
  const tid = setTimeout(() => controller.abort(), TIMEOUT_MS);
  try {
    const res = await fetch(url, {
      method: 'GET',
      redirect: 'follow',
      signal: controller.signal,
      headers: { 'User-Agent': 'JLS-redirect-verify/1.0' },
    });
    clearTimeout(tid);
    return { status: res.status, finalUrl: res.url };
  } catch (e) {
    clearTimeout(tid);
    return { status: e.name === 'AbortError' ? 'TIMEOUT' : String(e.message), finalUrl: url };
  }
}

async function parseRedirectRules() {
  const text = await fs.readFile(REDIRECTS, 'utf8');
  /** @type {Array<{ src: string; dest: string }>} */
  const rules = [];
  for (const line of text.split('\n')) {
    const t = line.trim();
    if (!t || t.startsWith('#')) continue;
    const parts = t.split(/\s+/);
    if (parts.length < 3 || parts[2] !== '301') continue;
    rules.push({ src: parts[0], dest: parts[1] });
  }
  return rules;
}

async function main() {
  const allRules = await parseRedirectRules();
  const malformed = allRules.filter(
    (r) =>
      r.src.includes('%29%2A%2A') ||
      r.src.includes(')**') ||
      (r.src.endsWith('*') && /\/service-areas\/(ada|lawton)\*$/.test(r.src))
  );
  const literalMalformed = [
    { src: '/service-areas/ada)**', dest: '/service-areas/ada' },
    { src: '/service-areas/lawton)**', dest: '/service-areas/lawton' },
  ];
  const countyShort = allRules.filter(
    (r) => r.src.startsWith('/counties/') && !r.src.endsWith('-county') && !r.src.includes(')**')
  );
  const sampleCounty = countyShort.filter((_, i) => i % 10 === 0);
  const bingCountyFails = [
    { src: '/counties/payne', dest: '/counties/payne-county' },
    { src: '/counties/tulsa', dest: '/counties/tulsa-county' },
    { src: '/counties/seminole', dest: '/counties/seminole-county' },
    { src: '/counties/pawnee', dest: '/counties/pawnee-county' },
    { src: '/counties/texas', dest: '/counties/texas-county' },
    { src: '/counties/le-flore', dest: '/counties/leflore-county' },
  ];
  const compareRedirect = [
    { src: '/compare/process-server-vs-sheriff', dest: '/process-server-vs-sheriff' },
  ];
  const toTest = [...malformed, ...literalMalformed, ...compareRedirect, ...bingCountyFails, ...sampleCounty];

  const results = [];
  for (const { src, dest } of toTest) {
    const sourceUrl = `${BASE}${src}`;
    const expectedDest = `${BASE}${dest}`;
    const first = await headNoFollow(sourceUrl);
    const chain = await headFollow(sourceUrl);
    const redirectOk =
      typeof first.status === 'number' &&
      first.status >= 301 &&
      first.status < 400;
    const destOk =
      typeof chain.status === 'number' &&
      chain.status >= 200 &&
      chain.status < 400 &&
      chain.finalUrl.replace(/\/$/, '') === expectedDest.replace(/\/$/, '');
    results.push({
      src,
      dest,
      firstStatus: first.status,
      location: first.location,
      finalStatus: chain.status,
      finalUrl: chain.finalUrl,
      pass: redirectOk && destOk,
    });
  }

  const failed = results.filter((r) => !r.pass);
  const now = new Date().toISOString();
  let md = `<!-- Generated by scripts/verify-redirects.mjs — DO NOT edit by hand -->\n\n`;
  md += `# Redirect HTTP verification (Layer 2)\n\n`;
  md += `- **When:** ${now}\n`;
  md += `- **Base:** ${BASE}\n`;
  md += `- **Rules tested:** ${results.length} (${malformed.length} malformed + ${sampleCounty.length} county sample of ${countyShort.length})\n`;
  md += `- **Failed:** ${failed.length}\n\n`;

  if (failed.length === 0) {
    md += `All tested redirect sources returned **3xx** without follow, then **2xx** at canonical destination.\n\n`;
  } else {
    md += `## Failures\n\n| Source | Expected | First | Final | Pass |\n|--------|----------|-------|-------|------|\n`;
    for (const r of failed) {
      md += `| ${r.src} | ${r.dest} | ${r.firstStatus} | ${r.finalStatus} | no |\n`;
    }
    md += `\n`;
  }

  md += `## All tested\n\n| Source | Dest | First | Final URL | Pass |\n|--------|------|-------|-------------|------|\n`;
  for (const r of results) {
    md += `| ${r.src} | ${r.dest} | ${r.firstStatus} | ${r.finalUrl.replace(BASE, '')} | ${r.pass ? 'yes' : 'no'} |\n`;
  }

  await fs.mkdir(path.dirname(OUT), { recursive: true });
  await fs.writeFile(OUT, md, 'utf8');
  console.log(`Wrote ${OUT}`);
  const encodedFails = failed.filter((r) => r.src.includes('%29%2A%2A'));
  const countyFails = failed.filter((r) => !r.src.includes('%29%2A%2A'));
  console.log(`Pass: ${results.length - failed.length}/${results.length}`);
  if (encodedFails.length) {
    console.warn(
      `Encoded malformed redirects (${encodedFails.length}) need redeploy (10a in _redirects).`
    );
  }
  if (countyFails.length) process.exit(1);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
